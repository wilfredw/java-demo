package com.wei.java.math;

public class GetNumberFromDouble {
    public static void main(String[] args) {
        double value = Math.ceil(1.4);
        System.out.println(value);
        value = Math.ceil(1.5);
        System.out.println(value);
        value = Math.ceil(1.6);
        System.out.println(value);

        value = Math.round(1.4);
        System.out.println(value);
        value = Math.round(1.5);
        System.out.println(value);
        value = Math.round(1.6);
        System.out.println(value);

        value = Math.floor(1.4);
        System.out.println(value);
        value = Math.floor(1.5);
        System.out.println(value);
        value = Math.floor(1.6);
        System.out.println(value);

        value = Math.ceil(-1.4);
        System.out.println(value);
        value = Math.ceil(-1.5);
        System.out.println(value);
        value = Math.ceil(-1.6);
        System.out.println(value);

        value = Math.round(-1.4);
        System.out.println(value);
        value = Math.round(-1.5);
        System.out.println(value);
        value = Math.round(-1.6);
        System.out.println(value);

        value = Math.floor(-1.4);
        System.out.println(value);
        value = Math.floor(-1.5);
        System.out.println(value);
        value = Math.floor(-1.6);
        System.out.println(value);

    }
}
